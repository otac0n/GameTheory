namespace GameTheory.Games.CenturySpiceRoad
{
    /// <summary>
    /// Represents a spice.
    /// </summary>
    public enum Spice : byte
    {
        /// <summary>
        /// Represents the yellow spice Turmeric.
        /// </summary>
        Turmeric = 0,

        /// <summary>
        /// Represents the red spice Saffron.  Spelled in the official rules as "safran".
        /// </summary>
        Saffron = 1,

        /// <summary>
        /// Represents the green spice Cardamom.
        /// </summary>
        Cardamom = 2,

        /// <summary>
        /// Represents the brown spice Cinnamon.
        /// </summary>
        Cinnamon = 3,
    }
}
